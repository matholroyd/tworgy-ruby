require File.expand_path(File.dirname(__FILE__) + '../../../spec_helper')

describe 'rspec helper' do
  it 'should capture nothing' do
    capture_stderr {
      1 + 1
      1.to_s
    }.should == ""
  end 
   
  it 'should capture errors on stderr' do
    capture_stderr {
      [].id
    }.should =~ /Object#id will be deprecated/
  end
  
end